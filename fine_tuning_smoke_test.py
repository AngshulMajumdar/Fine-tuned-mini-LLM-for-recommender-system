batch_df = df.head(5)
batch_payload = {
    "pairs": [
        {"user_id": int(r["user_id"]), "item_id": int(r["item_id"])}
        for _, r in batch_df.iterrows()
    ]
}

resp2 = requests.post("http://127.0.0.1:8000/predict_batch", json=batch_payload, timeout=60)
