import pandas as pd
import requests

TEST_CSV = "/content/drive/MyDrive/movielens_100k_ft_distilgpt2_steps300_clean/prepared/test_examples.csv"
df = pd.read_csv(TEST_CSV)

row = df.iloc[0]
payload = {
    "user_id": int(row["user_id"]),
    "item_id": int(row["item_id"])
}

resp = requests.post("http://127.0.0.1:8000/predict", json=payload, timeout=60)
